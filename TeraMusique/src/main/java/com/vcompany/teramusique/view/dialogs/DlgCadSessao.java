    private Object getObjetoSelecionadoNaGrid() {
        int rowCliked = grdTodasAsSessoes.getSelectedRow();
        Object obj = null;
        if (rowCliked >= 0) {
            obj = grdTodasAsSessoes.getModel().getValueAt(rowCliked, -1);
        }
        return obj;
    }


