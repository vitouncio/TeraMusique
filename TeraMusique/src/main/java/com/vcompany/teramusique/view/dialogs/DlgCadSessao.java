    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        Sessao sessaoExcluida = (Sessao) this.getObjetoSelecionadoNaGrid();

        if (sessaoExcluida == null)
            JOptionPane.showMessageDialog(this, "Primeiro, selecione um registro na tabela. ");
        else {
            int response = JOptionPane.showConfirmDialog(null,
                    "Deseja excluir a Sessao \n( "
                    + sessaoExcluida.getId() + " ), de Musicoterapeuta responsàvel: "
                    + sessaoExcluida.getMusicoterapeutaResp().toString() + "?",
                    "Confirmar exclusão",
                    JOptionPane.OK_CANCEL_OPTION,
                    JOptionPane.QUESTION_MESSAGE);
            if (response == JOptionPane.OK_OPTION) {
                try {
                    sCont.excluirSessao(sessaoCorrente);
                    sCont.atualizarTabela(grdTodasAsSessoes);
                    JOptionPane.showMessageDialog(this, "Exlusão feita com sucesso!");
                } catch (SessaoException e) {
                    JOptionPane.showMessageDialog(this, e.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

