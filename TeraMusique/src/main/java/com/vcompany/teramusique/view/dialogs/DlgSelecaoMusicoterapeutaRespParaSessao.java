    private Object getObjetoSelecionadoNaGrid() {
        int rowClicked = grdMusicoterapeutasDisponiveis.getSelectedRow();
        Object obj = null;
        if (rowClicked >= 0) {
            obj = grdMusicoterapeutasDisponiveis.getModel().getValueAt(rowClicked, -1);
        }
        return obj;
    }
