    private void grdMusicoterapeutasDisponiveisMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_grdMusicoterapeutasDisponiveisMouseClicked
        // TODO add your handling code here:
        musicoterapeutaEscolhido = (Musicoterapeuta) this.getObjetoSelecionadoNaGrid();

        if (musicoterapeutaEscolhido == null) {
            JOptionPane.showMessageDialog(this, "Pirmeiro, selecione um registro na tabela.");
        } else {
            txtMusicoterapeutasSelecionados.setText(musicoterapeutaEscolhido.toString());
        }

    }//GEN-LAST:event_grdMusicoterapeutasDisponiveisMouseClicked

    private void edtFiltrarNomeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_edtFiltrarNomeKeyPressed
        // TODO add your handling code here:
        String nomeDigitado = edtFiltrarNome.getText();
        if (!nomeDigitado.isEmpty())
            this.mCont.atualizarTabela(grdMusicoterapeutasDisponiveis, nomeDigitado);
        else
            this.mCont.atualizarTabela(grdMusicoterapeutasDisponiveis);
    }//GEN-LAST:event_edtFiltrarNomeKeyPressed

    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:
        musicoterapeutaEscolhido = (Musicoterapeuta) this.getObjetoSelecionadoNaGrid();

        if (musicoterapeutaEscolhido == null) {
            int response = JOptionPane.showConfirmDialog(null,
                    "Nenhum item foi escolhido. Deseja continuar?",
                    "Confirm",
                    JOptionPane.OK_CANCEL_OPTION,
                    JOptionPane.QUESTION_MESSAGE);

            if (response == JOptionPane.OK_OPTION) {
                dispose();
            }
        } else {
            if (sessaoSendoCadastrada.getMusicoterapeutaResp().equals(musicoterapeutaEscolhido)) {
                JOptionPane.showMessageDialog(null, "Este musicoterapeuta já está cadastrado.");
            } else if (sessaoSendoCadastrada.getLstMusicoterapeutas() == null) {
                JOptionPane.showMessageDialog(null, "Lista de musicoterapeutas vazia.");
            } else {
                this.sessaoSendoCadastrada.setMusicoterapeutaResp(musicoterapeutaEscolhido);
                dispose();

            }
        }

    }//GEN-LAST:event_btnAdicionarActionPerformed


